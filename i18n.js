function applyTranslations(lang) {
  const dict = translations[lang];
  if (!dict) return;

  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'he' ? 'rtl' : 'ltr';

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    const value = dict[key];
    if (value === undefined) return;

    if (el.tagName === 'META') {
      el.setAttribute('content', value);
    } else if (el.tagName === 'TITLE') {
      el.textContent = value;
      document.title = value;
    } else {
      el.textContent = value;
    }
  });

  document.querySelectorAll('.lang-option').forEach((el) => {
    el.classList.toggle('active', el.getAttribute('data-lang') === lang);
  });
}

function initI18n() {
  const saved = localStorage.getItem('propost-lang');
  const lang = saved || 'he';

  applyTranslations(lang);

  const toggleHandler = (e) => {
    const target = e.target.closest('.lang-option');
    if (!target) return;
    const newLang = target.getAttribute('data-lang');
    if (newLang && translations[newLang]) {
      localStorage.setItem('propost-lang', newLang);
      applyTranslations(newLang);
    }
  };

  const toggle = document.getElementById('langToggle');
  if (toggle) toggle.addEventListener('click', toggleHandler);
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initI18n);
} else {
  initI18n();
}
